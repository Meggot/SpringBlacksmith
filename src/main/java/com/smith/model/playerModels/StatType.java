package com.smith.model.playerModels;

public enum  StatType {
    Stamina, //Used to decide health
    Damage, //Used to decide raw damage from physical weapons
    Agilty, //Used to decide dodge and parry values
    Focus, //Used to decide accuracy and range of ranged weapons
    CriticalStrike, //crit multiplier
    Mana, //used to decide how many spells or how long he can channel
    AbilityPower;// Used to decide spell strength
}
